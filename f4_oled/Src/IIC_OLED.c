/**********************************************
//IIC Start
**********************************************/
void IIC_Start()
{
   SCL_1;		
   SDA_1;
	 IIC_delay();
   SDA_0;
	 IIC_delay();
   SCL_0;
	 IIC_delay();
}

/**********************************************
//IIC Stop
**********************************************/
void IIC_Stop()
{
 //  SCL_0;
   SDA_0;
   SCL_1;
	 IIC_delay();
   SDA_1;
}

/**********************************************
// 通过I2C总线写一个字节
**********************************************/
  void Write_IIC_Byte(unsigned char IIC_Byte)
  {
    unsigned char i;
    for(i=0;i<8;i++)             
    {
            if(IIC_Byte & 0x80)
                    SDA_1;
            else
                    SDA_0;
            SCL_1;              
            SCL_0;
            IIC_Byte<<=1;     
    }
		IIC_delay();
    SDA_1;
		IIC_delay();
    SCL_1;
    SCL_0;
  }

/*********************OLED写数据************************************/ 
void OLED_WrDat(unsigned char IIC_Data)
{
	IIC_Start();                                //地址0111 100+LSB AD0 = 0 so address = 0x78
	Write_IIC_Byte(OLED_ADDR);                   //0111100->Slave address, 0->SA0=0
	Write_IIC_Byte(0x40);			//write data
	Write_IIC_Byte(IIC_Data);
	IIC_Stop();
}
/*********************OLED写命令************************************/
void OLED_WrCmd(unsigned char IIC_Command)
{
	IIC_Start();
	Write_IIC_Byte(OLED_ADDR);            //0111100->Slave address, 0->SA0=0
	Write_IIC_Byte(0x00);			//write command
	Write_IIC_Byte(IIC_Command);
	IIC_Stop();
}
/*********************OLED 设置坐标************************************/
void OLED_Set_Pos(unsigned char x, unsigned char y) 
{ 
	OLED_WrCmd(0xb0+y);           //设置page，范围b0~b7
	OLED_WrCmd(((x&0xf0)>>4)|0x10);  //set the upper column address
       
   
	OLED_WrCmd((x&0x0f)|0x01);//set the lower column address
} 
/*********************OLED全屏************************************/
void OLED_Fill(unsigned char bmp_dat) 
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
		OLED_WrDat(bmp_dat);
	}
}
/*********************OLED复位************************************/
void OLED_CLS(void)
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
		OLED_WrDat(0);
	}
}
/*********************OLED初始化************************************/
void OLED_Init(void)
{

	//delay(500);//初始化之前的延时很重要！
	IIC_delay();
	
	OLED_WrCmd(0xae);//--turn off oled panel
	IIC_delay();
	OLED_WrCmd(0x00);//---set low column address
	IIC_delay();
	OLED_WrCmd(0x10);//---set high column address
	IIC_delay();
	OLED_WrCmd(0x40);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
	IIC_delay();
	OLED_WrCmd(0x81);//--set contrast control register
	IIC_delay();
	OLED_WrCmd(Brightness); // Set SEG Output Current Brightness
	IIC_delay();
	OLED_WrCmd(0xa1);//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
	IIC_delay();
	OLED_WrCmd(0xc8);//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
	IIC_delay();
	OLED_WrCmd(0xa6);//--set normal display
	IIC_delay();
	OLED_WrCmd(0xa8);//--set multiplex ratio(1 to 64)
	IIC_delay();
	OLED_WrCmd(0x3f);//--1/64 duty
	IIC_delay();
	OLED_WrCmd(0xd3);//-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
	IIC_delay();
	OLED_WrCmd(0x00);//-not offset
	IIC_delay();
	OLED_WrCmd(0xd5);//--set display clock divide ratio/oscillator frequency
	IIC_delay();
	OLED_WrCmd(0x80);//--set divide ratio, Set Clock as 100 Frames/Sec
	IIC_delay();
	OLED_WrCmd(0xd9);//--set pre-charge period
	IIC_delay();
	OLED_WrCmd(0xf1);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	IIC_delay();
	OLED_WrCmd(0xda);//--set com pins hardware configuration
	IIC_delay();
	OLED_WrCmd(0x12);
	IIC_delay();
	OLED_WrCmd(0xdb);//--set vcomh
	IIC_delay();
	OLED_WrCmd(0x40);//Set VCOM Deselect Level
	IIC_delay();
	OLED_WrCmd(0x20);//-Set Page Addressing Mode (0x00/0x01/0x02)
	IIC_delay();
	OLED_WrCmd(0x02);//
	IIC_delay();
	OLED_WrCmd(0x8d);//--set Charge Pump enable/disable
	IIC_delay();
	OLED_WrCmd(0x14);//--set(0x10) disable
	IIC_delay();
	OLED_WrCmd(0xa4);// Disable Entire Display On (0xa4/0xa5)
	IIC_delay();
	OLED_WrCmd(0xa6);// Disable Inverse Display On (0xa6/a7) 
	IIC_delay();
	OLED_WrCmd(0xaf);//--turn on oled panel
	IIC_delay();
	OLED_Fill(0x00); //初始清屏
	IIC_delay();
	OLED_Set_Pos(0,0);
} 
