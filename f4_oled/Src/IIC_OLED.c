//显示5个数字的数字串
void disnum(unsigned int num,unsigned char x, unsigned char y)
{
  unsigned char number[]={"     "};
  unsigned char i,j,c;
  number[4] = num%10 ;
  number[3] = (num/10)%10 ;
  number[2] = (num/100 )%10;
  number[1] = (num/1000 )%10 ;
  number[0] = num/10000 ;
  OLED_Set_Pos(x,y);
  for(j=0;j<5;j++)
    {
      c = number[j] + 16 ;
      for(i=0;i<6;i++)  OLED_WrDat(F6x8[c][i]);
    }
}

/***************功能描述：显示6*8一组标准ASCII字符串	显示的坐标（x,y），y为页范围0～7****************/
void OLED_P6x8Str(unsigned char x, unsigned char y, char ch[])
{
	unsigned char c=0,i=0,j=0;
	while (ch[j]!='\0')
	{
		c =ch[j]-32;
		if(x>126){x=0;y++;}
		OLED_Set_Pos(x,y);
		for(i=0;i<6;i++)
		OLED_WrDat(F6x8[c][i]);
		x+=6;
		j++;
	}                                                                                                                                                                                                                                                                                            
}
